function applyLanguage(lang) {
    var dict = translations[lang];
    if (!dict) return;

    // Text content
    document.querySelectorAll("[data-i18n]").forEach(function (el) {
        var key = el.getAttribute("data-i18n");
        if (dict[key] != null) {
            el.textContent = dict[key];
        }
    });

    // innerHTML for elements with mixed content (span inside)
    document.querySelectorAll("[data-i18n-html]").forEach(function (el) {
        var key = el.getAttribute("data-i18n-html");
        if (dict[key] != null) {
            el.innerHTML = dict[key];
        }
    });

    // Placeholders
    document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
        var key = el.getAttribute("data-i18n-placeholder");
        if (dict[key] != null) {
            el.placeholder = dict[key];
        }
    });

    // Update <html lang>
    var langMap = { en: "en", pt: "pt-BR", es: "es" };
    document.documentElement.lang = langMap[lang] || lang;

    // Update active state on language buttons
    document.querySelectorAll("[data-lang]").forEach(function (btn) {
        if (btn.getAttribute("data-lang") === lang) {
            btn.classList.add(
                "text-on-primary-container",
                "font-bold",
                "bg-primary-container",
            );
            btn.classList.remove("text-on-surface-variant", "bg-transparent");
        } else {
            btn.classList.remove(
                "text-on-primary-container",
                "font-bold",
                "bg-primary-container",
            );
            btn.classList.add("text-on-surface-variant", "bg-transparent");
        }
    });

    localStorage.setItem("lang", lang);
}
